package percabangan;

public class Switch {
    public static void main(String[] args){
        int jalanJalan=1;
        switch(jalanJalan){
            case 1:
                System.out.println("achmad riduan amin");
                break;
            case 2:
                System.out.println("urang acayan");
                break;
            case 3:
                System.out.println("ubat nyamuk");
                break;
            case 4:
                System.out.println("dubil dit");
                break;
            default:
                System.out.println("makhluk halus");
                break;
        }
    }
}
